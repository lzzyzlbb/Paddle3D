from typing import Tuple

import numpy as np


def horizontal_flip(im: np.ndarray) -> np.ndarray:
    if len(im.shape) == 3:
        im = im[:, ::-1, :]
    elif len(im.shape) == 2:
        im = im[:, ::-1]
    return im


def normalize(im: np.ndarray, mean: Tuple[float, float, float],
              std: Tuple[float, float, float]) -> np.ndarray:
    im = im.astype(np.float32) / 255.0
    im -= mean
    im /= std
    return im
